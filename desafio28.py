# Recebendo a placa do carro
placa = int(input['Digite o primeiro numero da placa do carro: '])

# Verificando o ultimo numero de cada placa para saber qual sera o mes de vencimento do IPVA
if placa[3] == 0:
    print('O mes de vencimento do IPVA é Janeiro!')
elif placa[3] == 1:
    print('O mes de vencimento do IPVA é Fevereiro!')
elif placa[3] == 2:
    print('O mes de vencimento do IPVA é Março!')
elif placa[3]  == 3:
    print('O mes de vencimento do IPVA é Abril!')
elif placa[3] == 4:
    print('O mes de vencimento do IPVA é Maio!')
elif placa[3] == 5:
    print('O mes de vencimento do IPVA é Junho!')
elif placa[3] == 6:
    print('O mes de vencimento do IPVA é Julho!')
elif placa[3] == 7:
    print('O mes de vencimento do IPVA é Agosto!')
elif placa[3] == 8:
    print('O mes de vencimento do IPVA é Setembro!')
elif placa[3] == 9:
    print('O mes de vencimento do IPVA é Outubro!')